    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       try {
            Encargados enc= ge.BuscarEncargados(txtId.getText());
            if (enc != null) {
                //se ubican los resultados obtenidos de la funcion ConsultarUsuarios
                txtId.setText(enc.getId());
                txtCI.setText(enc.getCedula());
                txtNombre.setText(enc.getNombre());
                txtTelefono.setText(enc.getTelefono());
                txtEmail.setText(enc.getEmail());
                txtDireccion.setText(enc.getDireccion());
                 if (enc.getSexo().equals("Masculino")){
                        rbtMasculio.setSelected(false);
                    } else {
                        rbtFemenino.setSelected(false);
                    }
                txtObservaciones.setText(enc.getObservaciones());      
            }
        } catch (SQLException ex) {
            Logger.getLogger(Contenidos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       if (ge.eliminarEncargado(txtId.getText()) == true) {
            JOptionPane.showMessageDialog(null, "Encargado eliminado exitosamente");
            //llamo a la funcion limpiar para limpiar las entradas 
            Limpiar();
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo eliminar");
        }
        Limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed

