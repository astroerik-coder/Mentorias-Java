    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            Emprendedores emprendedor = ge.BuscarEmprendedor(txtId.getText());
            if (emprendedor != null) {
                //se ubican los resultados obtenidos de la funcion ConsultarUsuarios
                txtId.setText(emprendedor.getId());
                txtNombre.setText(emprendedor.getNombre());
                txtTelefono.setText(emprendedor.getTelefono());
                    
                if (emprendedor.getSexo().equals("Masculino")){
                        rbtMasculio.setSelected(false);
                    } else {
                        rbtFemenino.setSelected(false);
                    }
                txtEmail.setText(emprendedor.getEmail());
                txtDireccion.setText(emprendedor.getDireccion());
                txtNombreTienda.setText(emprendedor.getNombreTienda());
                txtDescripTienda.setText(emprendedor.getDescripcionTienda());
                txtMapsTienda.setText(emprendedor.getGoogleMaps());
                txtFbTienda.setText(emprendedor.getFacebookTienda());
                txtDireccionTienda.setText(emprendedor.getDireccionTienda());
                txtObservaciones.setText(emprendedor.getObservaciones());   
            }
        } catch (SQLException ex) {
            Logger.getLogger(Contenidos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

